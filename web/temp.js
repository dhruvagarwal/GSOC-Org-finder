function match(term,text) {
	text = text.toLowerCase().trim();
	term = term.toLowerCase().trim();
	// TODO: method to be changed and optimised

	return text.indexOf(term)!=-1;
}

function process_name(search) {
	var results = [];
	for (var i in db) {
		if (match(search,db[i]["name"])) {
			results.push(i);
		};
	};

	return results;
}

function process_count(search,type) {
	// 0,1,2 : for <,=,> resp.
	var results = [];
	search = parseInt(search);
	for (var i in db) {
		switch(type) {
			case 0:
				if (db[i]["count"]<search) {
					results.push(i);
				};
				break;
			case 1:
				if (db[i]["count"]==search) {
					results.push(i);
				};
				break;
			case 2:
				if (db[i]["count"]>search) {
					results.push(i);
				};
				break;
		}
	};
	// TODO: write above code properly
	return results;
}

function match_years(a,b) {
	var score = 0;
	for (var i = 0; i < a.length; i++) {
		if (b.indexOf(a[i])>-1) {
			score++;
		};
	};
	return score;
}

function process_years(search) {
	// considering search is an array
	var results = [];
	var len = search.length;
	for (var i in years) {
		if (len == match_years(search,years[i])) {
			results.push(i);
		};
	};
	return results;
}

function process_tags(search) {
	// considering search is an array
	// tags input to be sanitised and transformed to array while input
	var results = [];
	for (var i in db) {
		var score = 0;
		for (var j = 0; j < search.length; j++) {
			for (var k = 0; k < db[i]["tags"].length; k++) {
				if (match(search[j],db[i]["tags"][k])) {
					score++;
				};
			};
		};
		// the criteria can be later changed
		if (score == search.length) {
			results.push(i);
		};
	};
	return results;
}

function makecard(keyword) {
	// make card for an ORG
	var card = '<div id="card">\n';
	card += '<div class="orgName">' + db[keyword]["name"] + '</div>\n';
	card += '<div class="caption">Years</div>\n<div class="orgYears">' + years[keyword].join() + '</div>\n';
	card += '<div class="caption" id="toggletags"></div><div class="orgTags">' + db[keyword]["tags"].join() + '</div><br />\n';
	card += '<div class="orgIdeas"><a href="' + db[keyword]["ideas"] + '" target="_blank">Ideas Page</a></div>\n';
	card += '</div>';
	return card;
}

